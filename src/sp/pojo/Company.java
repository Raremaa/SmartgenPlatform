package sp.pojo;
//企业/机构表
public class Company {
	int Company_id;
	String Company_name;
	String Company_userName;
	String Company_phone;
	String Company_charterNumber;
	String Company_password;
	String Company_logo;
	String Company_location;
	int  Company_identity;
	String condition;
	
	public int getCompany_id() {
		return Company_id;
	}
	public void setCompany_id(int company_id) {
		Company_id = company_id;
	}
	public String getCompany_name() {
		return Company_name;
	}
	public void setCompany_name(String company_name) {
		Company_name = company_name;
	}
	public String getCompany_userName() {
		return Company_userName;
	}
	public void setCompany_userName(String company_userName) {
		Company_userName = company_userName;
	}
	public String getCompany_phone() {
		return Company_phone;
	}
	public void setCompany_phone(String company_phone) {
		Company_phone = company_phone;
	}
	public String getCompany_charterNumber() {
		return Company_charterNumber;
	}
	public void setCompany_charterNumber(String company_charterNumber) {
		Company_charterNumber = company_charterNumber;
	}
	public String getCompany_password() {
		return Company_password;
	}
	public void setCompany_password(String company_password) {
		Company_password = company_password;
	}
	public String getCompany_logo() {
		return Company_logo;
	}
	public void setCompany_logo(String company_logo) {
		Company_logo = company_logo;
	}
	public String getCompany_location() {
		return Company_location;
	}
	public void setCompany_location(String company_location) {
		Company_location = company_location;
	}
	public int getCompany_identity() {
		return Company_identity;
	}
	public void setCompany_identity(int company_identity) {
		Company_identity = company_identity;
	}
	public String getCondition() {
		return condition;
	}
	public void setCondition(String condition) {
		this.condition = condition;
	}
	
}
